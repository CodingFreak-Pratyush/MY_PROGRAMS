package Java.Basics;

public class Arithmetic_Operators {
    public static void main(String[] args) {
        int a = 4;
        int b = 7;
        int sum = a + b;
        int diff = a - b;
        int multiply = a * b;
        int div = a / b;
        int modulo = a % b;
        System.out.println(sum);
        System.out.println(diff);
        System.out.println(multiply);
        System.out.println(div);
        System.out.println(modulo);

    }

}
