package Java.Basics;

public class MathClass {
    public static void main(String[] args) {

        // Java provides Maths class to perform various maths operation
        int a = 4;
        int b = 7;
        System.out.println(Math.max(a, b));
        System.out.println(Math.min(a, b));

        System.out.println(Math.random());// Random Member function Generates random numbers from 0.1 to 0.9
        System.out.println((int) (Math.random() * 100));// Random Member function Generates random numbers from 0.1 to
                                                        // 0.9

    }

}
