package Java.Basics;

public class Variables_DataTypes {
    public static void main(String[] args) {
        // Primitive Data Type : Primitive Types are DataTypes used to store simple
        // values
        // byte -1 [-127 to 127]
        // short - 2
        // int - 4
        // long - 8
        // float -4
        // double -8
        // char -2
        // boolean - 1

        byte age = 23;
        int marks = 7;
        long phone = 9601734609L;
        float pi = 3.14F;
        char letter = 'a';
        boolean isAdult = true;
        System.out.println(age);
        System.out.println(marks);
        System.out.println(phone);
        System.out.println(pi);
        System.out.println(letter);
        System.out.println(isAdult);

    }

}